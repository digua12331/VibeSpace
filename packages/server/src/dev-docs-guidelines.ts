export const DEV_DOCS_GUIDELINES = `# Dev Docs 工作流

本项目启用了 **Dev Docs 三段式工作流**。当用户提出一个**新功能、重构、bug 修复或任何非平凡的改动**时，你 **不要立刻改代码**，按以下流程推进。

流程整体**偏审慎多于速度**：宁可多确认一轮，也不在错误假设上跑远。琐碎到连任务都不构成的请求（格式化、改一个 typo、答疑），可以绕过本流程直接做，用自己的判断。

## 1. Plan 阶段 — 先规划

1. 从用户描述里提炼一个简短的 **任务名**（允许中文；禁止含 \`/ \\ : * ? " < > |\`）。不确定就先问用户。
2. 创建目录 \`dev/active/<任务名>/\`（若不存在）。
3. 写入 \`dev/active/<任务名>/<任务名>-plan.md\`，内容包含以下段落：
   - **目标**：解决什么问题，以及**可验证的验收标准** —— 用能跑的测试、能执行的命令、能观察的指标/截图来表达，不要写"让它能用"这种弱目标。举例：
     - "加校验" → "对非法输入写一个失败的测试，然后让它通过"
     - "修 bug" → "写一个复现 bug 的测试，然后让它通过"
     - "重构 X" → "确保重构前后测试全部通过"
   - **非目标 (Non-Goals)**：一到三行，明确列出**本次任务不做**的事 —— 容易被顺带扯进来的相邻模块、看起来相关但不在本轮的功能。写出来是为了给自己划好发散边界，执行阶段越界时可以回头对照。
   - **实施步骤**：有序列表，粗粒度即可。每一步尽量带一个"如何验证"的小尾巴。
   - **边界情况**：本方案在什么极端输入、异常状态、边界条件下会失效？列出已知的、需要在实现/测试时覆盖的。
   - **风险与注意**：已知的坑、需要确认的**假设**、可能波及到"关键文件"之外的模块。
4. **停下**，把 plan 呈给用户看，等 **用户确认或提出修改**。不要进入下一阶段。

### 写 plan 时的硬性规则（贯穿第 3 步）

- **假设要显式**：心里在默认的前提、依赖的接口行为、数据量级、边界场景，都写出来；不确定就停下来问，别闷头替用户做选择。
- **多解读别沉默**：如果用户这句话可以理解成 A 也可以理解成 B，两个都列出来让用户选，不要自己挑一个就跑。
- **看到更简的路就推回**：如果发现有比用户提议更简单的办法，**先说出来**让用户决定。不要因为用户说了就闷头实现复杂方案。
- **不清楚就停**：读代码/看上下文之后发现有说不通的地方，停下来点出哪里不清楚，问用户。

## 2. Context 阶段 — 上下文盘点

用户确认 plan 后，写入 \`dev/active/<任务名>/<任务名>-context.md\`，内容包含：

- **关键文件**：会读/会改的文件清单，附上相关符号或行号范围。这张清单就是本次改动的**边界**——执行阶段原则上只动这里列的文件；真要溢出，先回来补 context。
- **决策记录**：为什么选 A 不选 B，权衡是什么。每个决策必须回答一句：**"资深工程师看到这个方案，会不会觉得过度设计？"** 如果会，就简化到不会为止。具体忌讳：
  - 不做用户没要的功能
  - 不做只用一次的抽象
  - 不做没人要求的"灵活性"或"可配置性"
  - 不为不可能发生的场景写错误处理
  - 200 行能解决的问题不写 500 行
- **依赖与约束**：上游 API、数据结构、兼容性要求。

写完同样 **停下**，等用户再次确认 context 无误。

## 3. Tasks 阶段 — 清单 + 执行

用户确认 context 后，写入 \`dev/active/<任务名>/<任务名>-tasks.md\`：

\`\`\`markdown
# <任务名> · 任务清单

- [ ] 步骤 1 → verify: <怎么验证这步完成了>
- [ ] 步骤 2 → verify: ...
- [ ] 步骤 3 → verify: ...
\`\`\`

每条任务都带 \`verify:\`，这样你可以自己跑、自己判断是否通过，不必每一步都来问用户。

然后 **开始执行**。执行节奏规则：

- 每完成一个步骤，**立即** 把对应行从 \`- [ ]\` 改成 \`- [x]\`，再做下一步；不要批量勾。
- 过程中若发现 plan 或 context 有遗漏，**先更新对应 md 文件**，再继续。
- 若途中发现 tasks 需要拆分或新增，随时修改 \`tasks.md\`。

### 执行时的硬性规则（外科式改动）

- **只碰必须碰的**：每一行改动都要能追溯回 plan/tasks 的某一条。看不顺眼的相邻代码、注释、格式，**一律不要动**。
- **不要顺手重构**：没坏的东西别"改进"，哪怕你觉得自己会写得更好。保持现有代码风格，即使你不认同。
- **看到无关的死代码就提一嘴，不要删**：在回复里告知用户"顺便看到 X 似乎没在用"，由用户决定是否另起任务清理。
- **自己引入的孤儿要清掉**：本次改动让某个 import/变量/函数不再被使用，就该在本次删掉。预先存在的死代码不在此列。
- **不要加投机性代码**：别写"以后可能用到"的抽象、配置点、错误处理。当下不需要就不写。
- **熔断：同一步骤连续失败 2–3 次就停**：如果 \`verify\` 没通过、报错没解决，**修改 2 次仍然不过就停手**。不要越改越大、不要为了凑绿灯去动任务范围之外的底层代码或测试用例。停下来，把错误日志、你试过的方案和当前的疑惑打印给用户，等人介入再继续。

## Issues 档案

当你在推进**当前任务**时，顺手发现跟本任务**无关**的可疑问题（死代码、看起来没引用的函数/变量、明显错字、可疑的逻辑、老旧注释、未被删干净的遗留等），**不要改代码**，而是**追加一条**到项目根下的 \`dev/issues.md\`。这份文件是全局问题池，不跟某个任务绑定。

### 追加格式（必须单行）

\`\`\`markdown
- [ ] <简要描述>（文件 <相对路径>[:行号]；上下文：<一句话>）
\`\`\`

- 每条必须**单行**——解析器按行切，多行续写会丢数据。长描述就压缩到括号里，或分号分隔。
- 新追加的条目统一用 \`- [ ]\`（未处理）；处理完才改成 \`- [x]\`。
- \`dev/issues.md\` 不存在就创建；存在就 append 一行。**不要**覆盖已有内容。

### 派来处理 issues 的 Claude 终端

用户在 UI 的「Dev Docs」→「问题」tab 点"派 Claude"按钮时，会把问题文本塞给一个新的 Claude 终端，指令里会要求你：

1. 读 \`dev/issues.md\` 找到对应条目，分析并修复。
2. **处理完一条**，就把 \`dev/issues.md\` 里**该行**的 \`- [ ]\` 改成 \`- [x]\`（不要删行，保留作历史）。
3. 修复涉及多文件时，在本轮回复里简述改动点，仍按外科式原则——只碰必须碰的。

### 和本工作流的边界

- 处理 issues 属于**小任务**，一般不需要走完整的 plan→context→tasks 三段式；直接改代码 + 勾选即可。如果某条 issue 实际上是一个大坑（牵连多模块），停下来跟用户对齐是否升级成常规任务。
- 不要在 \`dev/issues.md\` 之外另起其它"待办池"文件（如 TODO.md / BACKLOG.md），保持单一入口。

## 规则与边界

- \`tasks.md\` 由你 (AI) 独占维护，人类只读，不要等用户手动勾选。
- \`plan.md\` 和 \`context.md\` 两边都可改；用户若手动编辑了，以他们的版本为准。
- 任务完成（所有 \`- [ ]\` → \`- [x]\`）后 **不要自动归档**，等用户在 UI 的「Dev Docs」侧栏里点归档。
- 若用户在对话里明确说"小改动"/"就一行"/"按你想法做"，可以 **合并 plan+context 为一次输出**（两个文件都写，但合并一轮确认），再进入 tasks 阶段。**不能跳过写文件**。

## 上下文耗尽的衔接

如果你感觉上下文窗口快满：

1. 把当前进度和下一步明确写入 \`tasks.md\`（未完成的项 \`- [ ]\` 加备注说明卡在哪）和 \`context.md\`（新决策、新发现的文件）。
2. 告诉用户"上下文接近上限，请开新会话只说『继续 <任务名>』，我会读完三个 md 无缝接上。"
3. 新会话里，用户只需说 \`继续 <任务名>\`，你就先读 \`dev/active/<任务名>/\` 下三个 md，再继续。

## 开始一个新任务的首个动作

收到需求后，第一步 **不是读代码**，而是：

1. 心里过一遍上面的流程。
2. 告诉用户你将以任务名 \`<xxx>\` 启动 Plan 阶段。
3. 然后去读代码、写 plan.md。

---

**本工作流生效的信号**：

- diff 里没有跟本任务无关的改动
- 不再因为"过度实现"反复重写
- 澄清问题发生在动手之前，而不是事后返工
- PR 干净、聚焦，没有顺手重构或"顺便改进"
`;

